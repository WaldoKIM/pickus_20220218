    function prev_section_btn(){
        if($('#main_guide2').css('display') == 'block'){
            $('#main_guide1').show();
            $('#main_guide2').hide();
            $('#main_guide3').hide(); 
            $('#main_guide4').hide(); 
        } else if($('#main_guide3').css('display') == 'block'){
            $('#main_guide2').show();
            $('#main_guide1').hide();
            $('#main_guide3').hide(); 
            $('#main_guide4').hide(); 
        } else if($('#main_guide4').css('display') == 'block'){
            $('#main_guide3').show();
            $('#main_guide1').hide();
            $('#main_guide2').hide(); 
            $('#main_guide4').hide(); 
        }
    }

    function next_section_btn(){
        if($('#main_guide1').css('display') == 'block'){
            $('#main_guide2').show();
            $('#main_guide1').hide();
            $('#main_guide3').hide(); 
            $('#main_guide4').hide(); 
        } else if($('#main_guide2').css('display') == 'block'){
            $('#main_guide3').show();
            $('#main_guide1').hide();
            $('#main_guide2').hide(); 
            $('#main_guide4').hide(); 
        } else if($('#main_guide3').css('display') == 'block'){
            $('#main_guide4').show();
            $('#main_guide1').hide();
            $('#main_guide2').hide(); 
            $('#main_guide3').hide(); 
